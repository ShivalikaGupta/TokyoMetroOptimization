def dijkstra(f, t):

    q, seen, mins = [(0, f, [])], [], {f: 0}
    while q:
        (cost, v1, path) = heappop(q)
        if v1 not in seen:
            seen.append(v1)
            path2 = []
            for i in path:
                path2.append(i)
            path2.append(v1)
            path = path2
            if v1 == t:
                return (cost, path)

            for v2, c in connect_new[v1]:
                if v2 in seen:
                    continue
                prev = mins.get(v2, None)
                next = cost + c
                if prev is None or next < prev:
                    mins[v2] = next
                    heappush(q, (next, v2, path))

    return float("inf"), None
